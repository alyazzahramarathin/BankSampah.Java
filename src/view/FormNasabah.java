     private void tampilData() {

        NasabahController controller = new NasabahController();

        DefaultTableModel model =
                (DefaultTableModel) TableNasabah.getModel();

        model.setRowCount(0);

        for (Nasabah data : controller.getAllNasabah()) {

            model.addRow(new Object[]{
                data.getIdNasabah(),
                data.getNama(),
                data.getAlamat(),
                data.getNoHp()
            });

        }
    }


