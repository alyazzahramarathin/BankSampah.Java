    private void ButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSimpanActionPerformed
        // TODO add your handling code here:
        if(TextFieldIdNasabah.getText().trim().isEmpty() ||
        TextFieldNama.getText().trim().isEmpty() ||
        TextFieldAlamat.getText().trim().isEmpty() ||
        TextFieldNoHp.getText().trim().isEmpty()){

         JOptionPane.showMessageDialog(this,
                 "Semua data wajib diisi!");

         return;
     }
        Nasabah n = new Nasabah();

        n.setIdNasabah(Integer.parseInt(TextFieldIdNasabah.getText()));
        n.setNama(TextFieldNama.getText());
        n.setAlamat(TextFieldAlamat.getText());
        n.setNoHp(TextFieldNoHp.getText());

        NasabahController controller = new NasabahController();

        if(controller.simpan(n)){

            JOptionPane.showMessageDialog(this,
                    "Data berhasil disimpan");

            tampilData();

            ButtonResetActionPerformed(null);

    }//GEN-LAST:event_ButtonSimpanActionPerformed
    }
