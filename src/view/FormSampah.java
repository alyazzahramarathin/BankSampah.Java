    private void ButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEditActionPerformed
        // TODO add your handling code here:
        Sampah s = new Sampah();

        s.setIdSampah(Integer.parseInt(TextFieldIdSampah.getText()));
        s.setNamaSampah(TextFieldNamaSampah.getText());
        s.setHarga(Double.parseDouble(TextFieldHarga.getText()));

        SampahController controller = new SampahController();

        if(controller.edit(s)){

            JOptionPane.showMessageDialog(this,"Data berhasil diubah");

            tampilData();

        }
    }//GEN-LAST:event_ButtonEditActionPerformed

    private void ButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSimpanActionPerformed
        // TODO add your handling code here:
        if(TextFieldIdSampah.getText().trim().isEmpty() ||
        TextFieldIdSampah.getText().trim().isEmpty() ||
        TextFieldNamaSampah.getText().trim().isEmpty() ||
        TextFieldHarga.getText().trim().isEmpty()){

         JOptionPane.showMessageDialog(this,
                 "Semua data wajib diisi!");

         return;
        }
        Sampah s = new Sampah();

        s.setIdSampah(Integer.parseInt(TextFieldIdSampah.getText()));
        s.setNamaSampah(TextFieldNamaSampah.getText());
        s.setHarga(Double.parseDouble(TextFieldHarga.getText()));

        SampahController controller = new SampahController();
        if(controller.simpan(s)){
            JOptionPane.showMessageDialog(this,"Data berhasil disimpan");
            tampilData();
        }else{
            JOptionPane.showMessageDialog(this,"Data gagal disimpan");
        }
        TextFieldIdSampah.setText("");
        TextFieldNamaSampah.setText("");
        TextFieldHarga.setText("");
    }//GEN-LAST:event_ButtonSimpanActionPerformed

    private void ButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonHapusActionPerformed
        // TODO add your handling code here:
        String id = TextFieldIdSampah.getText();

        SampahController controller = new SampahController();
        if(controller.delete(id)){
            JOptionPane.showMessageDialog(this,"Data berhasil dihapus");
            tampilData();
        }
        TextFieldIdSampah.setText("");
        TextFieldNamaSampah.setText("");
        TextFieldHarga.setText("");
    }//GEN-LAST:event_ButtonHapusActionPerformed

