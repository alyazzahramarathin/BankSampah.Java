    private void tampilData(){

    DefaultTableModel model = (DefaultTableModel) TableSampah.getModel();
    model.setRowCount(0);
    SampahController controller = new SampahController();
    List<Sampah> list = controller.getAllSampah();

    for(Sampah s : list){
        Object[] row = {
            s.getIdSampah(),
            s.getNamaSampah(),
            s.getHarga()
        };
        model.addRow(row);
    }
}
    
