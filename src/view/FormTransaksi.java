    private void ButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSimpanActionPerformed
        // TODO add your handling code here:
        try {

        Transaksi t = new Transaksi();
        if (TextFieldBerat.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Berat belum diisi");
        return;}
        
        TextFieldIdTransaksi.setEditable(false);
        
        t.setTanggal(java.sql.Date.valueOf(TextFieldTanggal.getText()));
        
        Nasabah n = listNasabah.get(ComboBoxNasabah.getSelectedIndex());
        t.setIdNasabah(n.getIdNasabah());
        
        Sampah s = listSampah.get(ComboBoxSampah.getSelectedIndex());
        t.setIdSampah(s.getIdSampah());
        
        t.setBerat(Double.parseDouble(TextFieldBerat.getText()));
       t.setTotal(Double.parseDouble(TextFieldTotal.getText()));

        controller.simpan(t);

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
        tampilData();
        reset();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_ButtonSimpanActionPerformed

