    private void tampilData() {
    model.setRowCount(0);

    ArrayList<Transaksi> list = controller.getAll();

    for (Transaksi t : list) {

        Object[] row = {
            t.getIdTransaksi(),
            t.getTanggal(),
            t.getNamaNasabah(),
            t.getNamaSampah(),
            t.getBerat(),
            t.getHarga(),
            t.getTotal()
        };
        model.addRow(row);
    }
}
    public void loadNasabah() {
        
    listNasabah = nasabahDAO.getAllNasabah();
    ComboBoxNasabah.removeAllItems();
    for (Nasabah n : listNasabah) {
        ComboBoxNasabah.addItem(n.getNama());
    }

}
    private void loadSampah() {

    listSampah = sampahDAO.getAllSampah();
    ComboBoxSampah.removeAllItems();
    for (Sampah s : listSampah) {
        ComboBoxSampah.addItem(s.getNamaSampah());
    }
}
    private void hitungTotal() {
      try{

        if(TextFieldHarga.getText().trim().equals(""))
            return;
        if(TextFieldBerat.getText().trim().equals(""))
            return;
        double harga=Double.parseDouble(TextFieldHarga.getText());
        double berat=Double.parseDouble(TextFieldBerat.getText());
        double total=harga*berat;

        TextFieldTotal.setText(String.valueOf(total));
    }catch(Exception e){
        TextFieldTotal.setText("");
    }
}
    private void reset() {

    TextFieldTanggal.setText(new SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date()));
    TextFieldBerat.setText("");
    TextFieldHarga.setText("");
    TextFieldTotal.setText("");

    ComboBoxNasabah.setSelectedIndex(0);
    ComboBoxSampah.setSelectedIndex(0);

}
    
